// Tính chu vi và diện tích hình tròn
var chuvi = function (r) {
    var pi = 3.14;
    console.log("Chu vi l\u00E0: ".concat(2 * r * pi));
};
chuvi(5);
var dientich = function (r) {
    var pi = 3.14;
    return r * r * pi;
};
console.log("Di\u1EC7n t\u00EDch l\u00E0: ".concat(dientich(10)));
